require 'rails_helper'

RSpec.describe 'RecordPageFields', type: :feature do
  describe 'Title fields' do
    it 'User visits a document with a vernacular title' do
      pending('Vernacular title field will be displayed as title, title will appear below with label title')
    end

    it 'User visits a document without a vernacular title' do
      pending('Title field will be displayed as title')
    end

    it 'User visits a document with a uniform title' do
      pending('Uniform title field will be displayed')
    end

    it 'User visits a document with additional titles' do
      pending('Additional title field will be displayed')
    end

    it 'User visits a document with a series title' do
      pending('Series title field will be displayed')
    end
  end
end
